/* DCL Data Control Language
used to control access to a database,
responsible for defining the rights and roles
granted to individual users

Common SQL DCL statements:
GRANT
REVOKE
*/

/* DDL Data Definition Language
used to define the structure, or schema,
of a database
used to create, modify, and delete databases

Common SQL DDL statements:
CREATE
ALTER
DROP
*/

/* DML Data Manipulation Language
used to create, read, update, and delete
data in a database

Common SQL DML statements:
SELECT
UPDATE 
INSERT 
*/
